/*33.program to read two numbers and demostrate all bitwise operators using them*/
#include<stdio.h>
int main()
{
	int a,b;
	printf("\n enter a");
	scanf("%d",&a);
	printf("\n enter b");
	scanf("%d",&b);
	
	
	printf("Bitwise AND Operator a & b = %d\n", a&b);
	printf("Bitwise OR Operator a | b = %d\n", a|b);
	printf("Bitwise XOR Operator a ^ b = %d\n", a^b);
	printf("Bitwise NOT Operator ~a = %d\n",a = ~a);
	printf("\n\n\n\n");
	
	

	
return 0;
}
